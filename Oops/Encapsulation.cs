using System;
using System.Collections.Generic;
using System.Text;

namespace Oops
{
    internal class Encapsulation
    {
        private String name;
        private int age;

        public String Name
        {
            get
            {
                return name;
            }
            set
            {
                name = value;
            }
        }
        public int Age
        {
            get
            {
                return age;
            }
            set
            {
                age = value;
            }
        }
        
        

    }
    

}
